package kr.co.vo;

public class Depart {
	private String dp_id;
	private String dp_loadaddress;
	private String dp_gnumaddress;
	private double dp_ny;
	private double dp_nx;
	
	
	public String getDp_id() {
		return dp_id;
	}
	public void setDp_id(String dp_id) {
		this.dp_id = dp_id;
	}
	public String getDp_loadaddress() {
		return dp_loadaddress;
	}
	public void setDp_loadaddress(String dp_loadaddress) {
		this.dp_loadaddress = dp_loadaddress;
	}
	public String getDp_gnumaddress() {
		return dp_gnumaddress;
	}
	public void setDp_gnumaddress(String dp_gnumaddress) {
		this.dp_gnumaddress = dp_gnumaddress;
	}
	public double getDp_ny() {
		return dp_ny;
	}
	public void setDp_ny(double dp_ny) {
		this.dp_ny = dp_ny;
	}
	public double getDp_nx() {
		return dp_nx;
	}
	public void setDp_nx(double dp_nx) {
		this.dp_nx = dp_nx;
	}
	
	
	
}
